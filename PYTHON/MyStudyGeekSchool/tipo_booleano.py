# Ou (or)
'''
Operação binária que depende de dois valores
'''
print('\n\noperação or')
print(True or False)
print(False or True)
print(True or True)
print(False or False)

print('\n\noperação and')
print(True and False)
print(False and True)
print(True and True)
print(False and False)